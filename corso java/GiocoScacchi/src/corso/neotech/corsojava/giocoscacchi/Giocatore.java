package corso.neotech.corsojava.giocoscacchi;

public class Giocatore {
	
	
	private  String nome;
	private  int anni;
	private  int livelloAbilita;
	   
	   
	   
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getAnni() {
		return anni;
	}
	public void setAnni(int anni) {
		this.anni = anni;
	}
	public int getLivelloAbilita() {
		return livelloAbilita;
	}
	public void setLivelloAbilita(int livelloAbilita) {
		this.livelloAbilita = livelloAbilita;
	}
	   
	   
	    
	    
	    
	    
	    
	
	
	
	
	
	
	
	
	
	
	
	
}
